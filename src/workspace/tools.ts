export interface GenCtx {
  type: string
  category: string
  specs: { label: string; value: string }[]
  text: string
  format: string
  styleMode: 'copy' | 'inspire'
  hasRef: boolean
}

export interface ToolDef {
  id: string
  num: string
  name: string
  tag: string
  icon: LucideIcon
  desc: string
  kind: 'image' | 'text'
  needsRef?: boolean
  system?: string
  prompt: (c: GenCtx) => string
}

const adv = (c: GenCtx) => (c.text ? c.text.split('\n').map((l) => l.replace(/^[✦•\-\s]+/, '').trim()).filter(Boolean) : [])
const refLine = (c: GenCtx) => (c.hasRef ? 'Второе изображение — дизайн-референс: повтори его композицию, цвета и расположение текста. ' : '')
const base = (c: GenCtx) => `Первое изображение — фото товара пользователя (${c.type || c.category || 'товар'}). ${refLine(c)}Соотношение сторон кадра ${c.format}. Высокое качество, реалистично, без артефактов и лишнего текста.`

